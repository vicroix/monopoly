    private void GuardarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarPartidaActionPerformed
        FileOutputStream fos = null;
        ObjectOutputStream salida = null;
        //Guardar/Escribir en el fichero 
        try {
            fos = new FileOutputStream("jugadores.dat");

            salida = new ObjectOutputStream(fos);
//            System.out.println(jugador1);
//            System.out.println(jugador2);
//            System.out.println(turno);
            salida.writeObject(jugador1);
            salida.writeObject(jugador2);
            salida.writeObject(turno);
            JOptionPane.showMessageDialog(this, "El juego se guardo correctamente");
            if (fos != null) {
                fos.close();
            }

        } catch (IOException e) {
            System.out.println("Error" + e.toString());
            JOptionPane.showMessageDialog(this, "El juego no se pudo guardar");
        } finally {
            try {
                if (salida != null) {
                    salida.close();
                }

            } catch (IOException e) {
                System.out.println("Error: " + e.toString());
            }
        }
    }//GEN-LAST:event_GuardarPartidaActionPerformed

