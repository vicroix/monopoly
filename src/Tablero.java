    private void LanzarDadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LanzarDadoActionPerformed
        //Al jugador que toque su turno, se pone activa su JLebel en verde y el otro en gris.
        int valorCasilla = 0;

        
        if(jugador1.getPenalizacion() > 0 && jugador2.getPenalizacion() > 0){
        jugador1.setPenalizacion(jugador1.getPenalizacion()-1);
        jugador2.setPenalizacion(jugador2.getPenalizacion()-1);
        enviarInfoNuevoJugador(jugador1, TPenalizacionJugador1, TDineroJugador1, TDebesJugador1, TVictoriasJ1, TDerrotasJ1);
        enviarInfoNuevoJugador(jugador2, TPenalizacionJugador2, TDineroJugador2, TDebesJugador2, TVictoriasJ2, TDerrotasJ2);
        return;
        }                     
        else if (jugador1.getPenalizacion() > 0) {
            jugador1.setPenalizacion(jugador1.getPenalizacion() - 1);
            this.turno.setTurnoJugadores(false);
            enviarInfoNuevoJugador(jugador1, TPenalizacionJugador1, TDineroJugador1, TDebesJugador1, TVictoriasJ1, TDerrotasJ1);
        } else if (jugador2.getPenalizacion() > 0) {
            jugador2.setPenalizacion(jugador2.getPenalizacion() - 1);
            turno.setTurnoJugadores(true);
            enviarInfoNuevoJugador(jugador2, TPenalizacionJugador2, TDineroJugador2, TDebesJugador2, TVictoriasJ2, TDerrotasJ2);
        }

        if (turno.getTurnoJugadores()) {
            
            activarJugador(BJugador2);
            desactivarJugador(BJugador1);
            valorCasilla = moverCasillas(casillas, jugador1);
            jugador1.setBanco(jugador1.sumarDinero(valorCasilla, jugador1.getBanco()));
            enviarInfoNuevoJugador(jugador1, TPenalizacionJugador1, TDineroJugador1, TDebesJugador1, TVictoriasJ1, TDerrotasJ1);
//            System.out.println(jugador1);
            visualizarTablero(jugador1, Color.blue);

        } else {
            activarJugador(BJugador1);
            desactivarJugador(BJugador2);
            valorCasilla = moverCasillas(casillas, jugador2);
            jugador2.setBanco(jugador2.sumarDinero(valorCasilla, jugador2.getBanco()));
            enviarInfoNuevoJugador(jugador2, TPenalizacionJugador2, TDineroJugador2, TDebesJugador2, TVictoriasJ2, TDerrotasJ2);
//            System.out.println(jugador2);
            visualizarTablero(jugador2, Color.RED);

        }
        cambiarTurnos(turno);
    }//GEN-LAST:event_LanzarDadoActionPerformed

