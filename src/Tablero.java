    private void BotonIngresoUsuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonIngresoUsuariosActionPerformed
        //Obtengo el nombre de los jugadores a sus clase y los envio al panel de juego
        String TJugador1 = TIngresoJugador1.getText();
        this.TJugador1.setText(TJugador1);
        jugador1.setNombre(TJugador1);
        String TJugador2 = TIngresoJugador2.getText();
        this.TJugador2.setText(TJugador2);
        jugador2.setNombre(TJugador2);
        // Asigno el turno del jugador 1 o 2 como inicio de partida
        if (turno.getTurnoJugadores()) {
            activarJugador(BJugador1);
            desactivarJugador(BJugador2);
        } else {
            activarJugador(BJugador2);
            desactivarJugador(BJugador1);
        }
        //Activo el botón para poder lanzar el dado
        LanzarDado.setEnabled(true);
        visualizarTablero(jugador1, Color.blue);
        visualizarTablero(jugador2, Color.red);
        ResultadoDado.setText("");
        //Si los usuarios han ingresado un nombre el cuadro de Ingreso se pondra en invisible
        if (!TJugador1.equalsIgnoreCase("") && !TJugador2.equalsIgnoreCase("")) {
            IngresoUsuarios.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(this, "Error. Ingrese un nombre para los usuarios.");
        }
        //Cargar datos Jugador 1
        enviarInfoNuevoJugador(jugador1, TPenalizacionJugador1, TDineroJugador1, TDebesJugador1, TVictoriasJ1, TDerrotasJ1);
        //Cargar datos Jugador 2
        enviarInfoNuevoJugador(jugador2, TPenalizacionJugador2, TDineroJugador2, TDebesJugador2, TVictoriasJ2, TDerrotasJ2);
    }//GEN-LAST:event_BotonIngresoUsuariosActionPerformed

