    private int moverCasillas(Casillas casillas, Jugador jugador) {
        //Tirada de dados con número aleatorio
        int resultadoDado = dado.tirarDados();
        //Obtener posición del jugador
        int posicionJugador = jugador.getPosicion();
        //El jugador mueve ficha por el método valorCasilla, donde éste se encuentra en la clase Casillas
        int valorCasilla = casillas.valorCasilla(posicionJugador, resultadoDado, jugador);
        ResultadoDado.setText("Has sacado un " + resultadoDado);
        return valorCasilla;
    }

    //Con este método muestro una imagen ficha que pertenecera a cada jugador
    public void visualizarTablero(Jugador jugador, Color color) {
        desactivarCasillas(jugador);
        if (jugador.getNumeroJugador() == 1) {
            casillasJ1[jugador.getPosicion() - 1].setBackground(color);
        } else {
            casillasJ2[jugador.getPosicion() - 1].setBackground(color);
        }
    }

    private void desactivarCasillas(Jugador jugador) {
        Color gris = new Color(60, 63, 65);
        if (jugador.getNumeroJugador() == 1) {
            for (JPanel casilla : casillasJ1) {
                casilla.setBackground(gris);
            }
        } else {
            for (JPanel casilla : casillasJ2) {
                casilla.setBackground(gris);
            }
        }

    }

